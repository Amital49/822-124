package c.flowcontrol.ifConstruct;

public class Dem4Switch {

	public static void main(String[] args) {

		int key = 3;

		switch (key) {
		case 1:
			System.out.println("ON");
			break;
		case 2:
			System.out.println("OFF");
			break;
		case 3:
			System.out.println("STAND_BY");
			break;
		}

	}

}
