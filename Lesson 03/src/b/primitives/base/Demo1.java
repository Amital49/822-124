package b.primitives.base;

public class Demo1 {
	
	public static void main(String[] args) {
		// the value 16 in different bases
		int nDecimal = 16;
		int nBinary = 0b00010000; // base 2
		int nHexa = 0x10; // base 16 0 - 9 a, b, c, d, e, f
		int nOctal = 020; // base 8 (the digits 0 - 7)
		System.out.println(nBinary);
		System.out.println(nHexa);
		System.out.println(nOctal);
		
		System.out.println(1);
		System.out.println(5.3);
		System.out.println(5.3F);
		System.out.println("AAA");
	}

}
